def _write_shadcn_utils(lib_dir: Path) -> None:
    lib_dir.mkdir(parents=True, exist_ok=True)
    (lib_dir / "utils.ts").write_text('''import { type ClassValue, clsx } from "clsx";
import { twMerge } from "tailwind-merge";

export function cn(...inputs: ClassValue[]) {
  return twMerge(clsx(inputs));
}
''')


def _write_streamlit(cfg: ProjectConfig, target: Path) -> None:
    (target / "app.py").write_text(f"""import streamlit as st
import requests
import os

API_URL = os.getenv("API_URL", "http://localhost:3100")

st.title("{cfg.name}")

if "messages" not in st.session_state:
    st.session_state.messages = []

for msg in st.session_state.messages:
    with st.chat_message(msg["role"]):
        st.write(msg["content"])

if prompt := st.chat_input("Enter your message..."):
    st.session_state.messages.append({{"role": "user", "content": prompt}})
    with st.chat_message("user"):
        st.write(prompt)
    with st.chat_message("assistant"):
        with st.spinner("Thinking..."):
            try:
                response = requests.post(
                    f"{{API_URL}}/api/process",
                    json={{"input": prompt}},
                    timeout=60,
                )
                response.raise_for_status()
                output = response.json().get("output", "")
            except Exception as e:
                output = f"Error: {{e}}"
        st.write(output)
    st.session_state.messages.append({{"role": "assistant", "content": output}})
""")


def _write_chainlit(cfg: ProjectConfig, target: Path) -> None:
    (target / "app.py").write_text(f"""import chainlit as cl
import requests
import os

API_URL = os.getenv("API_URL", "http://localhost:3100")


@cl.on_chat_start
async def on_chat_start():
    await cl.Message(content="Hello! How can I help you?").send()


@cl.on_message
async def on_message(message: cl.Message):
    try:
        response = requests.post(
            f"{{API_URL}}/api/process",
            json={{"input": message.content}},
            timeout=60,
        )
        response.raise_for_status()
        output = response.json().get("output", "")
    except Exception as e:
        output = f"Error: {{e}}"
    await cl.Message(content=output).send()
""")
